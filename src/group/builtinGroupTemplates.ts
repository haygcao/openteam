export function getBuiltinGroupTemplate(templateId: string): BuiltinGroupTemplate | undefined {
  return BUILTIN_GROUP_TEMPLATES.find(template => template.id === templateId)
}

function group(
  id: string,
  name: string,
  category: string,
  summary: string,
  roles: BuiltinGroupTemplateRole[],
): BuiltinGroupTemplate {
  return {
    id,
    name,
    category,
    summary,
    defaultChatName: name,
    defaultMode: 'collaborative',
    roles,
  }
}

function role(name: string, description: string, systemPrompt: string): BuiltinGroupTemplateRole {
  return { name, description, systemPrompt }
}
